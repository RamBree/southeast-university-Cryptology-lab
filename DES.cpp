void ChangeToBi(bool* a, char c)//字符转化成二进制
{
	for (int i = 0; i < 8; i++)
	{

		if (((c >> i) & 0x1) == 1)
		{
			*(a + 7 - i) = true;
		}
		else
			*(a + 7 - i) = false;	
	}
}
char ChangeToChar(bool* a)//二进制转化成字符
{
	char c = '\0';
	for (int i = 0; i < 8; i++)
	{
		if (*(a + i)) c = (c << 1) | 1;
		else c = c << 1;
	}
	return  c;
}
inline void Equal(bool* a, bool* b, int n)
{
	for (int i = 0; i < n; i++)
		*(a + i) = *(b + i);
}
inline void Xor(bool* a, bool* b, int n)
{
	for (int i = 0; i < n; i++)
		*(a + i) = *(a + i) xor *(b + i);
}
